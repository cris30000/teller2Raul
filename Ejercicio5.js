/**
 * Imprimir los cuadrados de los enteros de 1 a 20.
 *
 * 
 */

let suma=0;

for(let i =1; i<=20; i++){
    let cuadrado = i*i;
    console.log("el cuadrado de el numero : " +i+ "  es: "+ cuadrado);
    suma += cuadrado;
}


console.log("la suma de los cuadrados es: "+suma);











